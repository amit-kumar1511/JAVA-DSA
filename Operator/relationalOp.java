public class relationalOp {
    public static void main(String[] args) {
        
        int x = 10;
        int y = 5;
        int z = 10;
        
        // == check eqality
        System.out.println(x==y);
        System.out.println(x==z);

        // != check not eqality
        System.out.println(x!=y);
        System.out.println(x!=z);

        // > greater trhen
        System.out.println(x>y);
        System.out.println(y>z);

        // < less then
        System.out.println(x<y);
        System.out.println(y<z);

        // >= greater then equal to
        System.out.println(x>=y);  //dono me se ek condition true hoga to ans true hoga
        System.out.println(y>=z);

        // <= less then equal to
        System.out.println(x<=y);  //dono me se ek condition true hoga to ans true hoga
        System.out.println(y<=z);


        
    }
}
