public class arthmaticOp {
    public static void main(String[] args) {
        
        int A  = 10;
        int B = 5;
        //binart op

        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(A%B);

         //unary op

         int a = 10;
         int b = ++a;  //pre increment
         System.out.println(a);
         System.out.println(b); 

         int x = 10;
         int y = x++;
         System.out.println(x);
         System.out.println(y);


    }
}
