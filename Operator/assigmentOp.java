public class assigmentOp {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;
      // =  
      a=b;
      System.out.println(a);

      //+=
      //a = a+10;
      a += 10;
      System.out.println(a);

      // -=
      a -= 10;  //a = a-10;
      System.out.println(a);

      // *=
      a *= 2;  //a = a*2;
      System.out.println(a);

    }
}
